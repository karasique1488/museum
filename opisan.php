<PRE><a  STYLE="font-family:Courier New Cyr; font-size:16px; font-weight:bold">

   Лувр в Париже – памятник архитектуры  и  богатейший музей,  не знающий равных
   себе по  разнообразию  и  полноте собраний,  их художественной и исторической
   ценности.  Первоначально Лувр – это королевский дворец,  построенный на месте
   замка. В 1793 декретом французской республики Лувр был объявлен общедоступным
   музеем, а его коллекции стали национальным достоянием. 
 </a>


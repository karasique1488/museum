<p align="justify">

<a STYLE="color:#000000; font-size:20px; font-weight:bold">Что такое Лувр?</a>
<br>
<br>


Один из величайших музеев мира. Королевский замок, гордость Франции, место действия романов Дюма. А еще – тюрьма и библиотека, сокровищница и заурядный средневековый замок… Фундамент замка XII века полностью раскрыли во дворе нынешнего Лувра уже в ХХ веке при археологических раскопках. Этот замок велел построить король Филипп II Август для обороны Парижа. В те времена в случае опасности Сену перегораживали цепями, чтобы в нее не мог войти вражеский флот. С одной стороны цепь крепилась к замку, с другой – к башне Нель, которая располагалась на левом берегу реки, как раз напротив замка. Король Филипп 2 АвгустТогда здесь был далекий пригород Парижа… 
<img src="http://aminpro.ru/dopoln/kartinki_d2/dopol/ludi/france/filip_2_avgust_01.jpg" alt="" title="" align="right" border="1" vspace="15" hspace="15">

Именно этот замок-крепость и стал основой будущего королевского дворца. Откуда взялось название Лувр – Louvre – не совсем понятно. Самая известная версия сводится к тому, что название родственно слову «Loup» – «Волк». Как будто бы здесь держали и разводили специальных собак для охоты на волков – «луврье». Другие исследователи привлекают для сравнения древнесаксонское слово «Lower» – «Крепость».

<br>
<br>
Кроме того, в текстах XII века упоминается деревня Лувр – Louvres, находившаяся к северу от парижского пригорода Сен-Дени, так что название это не было редким или необычным. Король Филипп Август, достойный противник знаменитого английского короля Ричарда Львиное Сердце, был крупным фортификатором. За время его правления во Франции было построено много крепостей по образцу столичной. Парижский же замок имел квадратную планировку, на каждом углу находилось по башне, а в центре вздымалась мощная цитадель тридцатиметровой высоты. Стены были окружены рвом. Цитадель служила одновременно главным укреплением крепости, арсеналом, сейфом, где хранились главные ценности королевства, архивом, где ревностно сохранялись документы, тюрьмой для важных заключенных. Кстати сказать, и документы, и пленники могли быть приравнены к ценностям – за них было можно получить немалую цену…
</p>

<p align="justify">

А сам Филипп II жил еще в королевском дворце на островке Ситэ. Королевской резиденцией Лувр стал позже. Столица разрасталась. В начале XIII века в ней жило сто двадцать тысяч человек, насчитывалось триста улиц, основные из которых были замощены. В середине XIV века король Карл V приказал окружить Париж новой крепостной стеной, и Лувр полностью потерял свое значение в системе обороны города. Туда переехал сам венценосец и перевез свою знаменитую библиотеку. Появилась специальная библиотечная башня. В ней хранилась тысяча рукописных книг, собранных королем, которого современники прозвали Мудрым. Это собрание впоследствии стало основой Национальной библиотеки Франции. Карл V Мудрый стремился придать жилой и уютный вид мрачному детищу Филиппа Августа. Были пристроены новые крылья дворца, над тяжелыми боевыми башнями вознеслись изящные остроконечные крыши и флагштоки.
<br>
<br>
Но запустение снова поселилось здесь – после смерти Карла V, и полвека замок стоял заброшенным. Короли и двор предпочитали парижские дворцы Сен-Поль и Турнель или же уютные замки в долине Луары. Тур, нынешний центр департаментов Эндр и Луара, мог в те годы реально вырвать пальму первенства у Парижа и победить в борьбе за право стать столицей Франции…
<br>
<br>
Следующая важная дата в истории Лувра – 1527 год. Король Франциск I, находясь в отчаянном положении, искал способ пополнить пустую казну и нашел его: он решил взять контрибуцию с парижан. Но чтобы подсластить пилюлю, король решил польстить тщеславию горожан. Он объявил, что иной столицы для прекрасной Франции не видит и возвращается жить в Париж.

<img src="http://aminpro.ru/dopoln/kartinki_d2/dopol/ludi/france/fracisk_01.jpg" alt="" title="" align="left" border="1" vspace="15" hspace="15">

В Лувре начались работы. Король Франциск IЦитадель была снесена, внешняя крепостная стена тоже – на ее месте разбили сад. Однако лишь через двадцать лет Франциск I отдал приказ приступить к строительству нового дворца на месте срытой крепости. Дальнейшую историю Лувра можно – при желании – свести к тому, при каком короле какой архитектор что пристроил, что перестроил и что снес. Всякий монарх делал это непременно, и по крайней мере этим входил в историю Франции. Великая Французская революция, например, и впрямь оказалась революцией для Лувра – она-то и превратила его в музей. Якобинцы решили устроить здесь «Центральный музеум искусств». А в годы революции и наполеоновских войн коллекция Лувра быстро разрослась за счет реквизиций у аристократов и конфискаций в ходе военных кампаний за границей… Вот так ход истории пополнял фонды музея! Коллекции музея «хорошели», чего нельзя было сказать о прилегающем к Лувру квартале, ставшем известным парижским гнездом преступности и нищеты. «Всякий человек, пусть даже приехавший в Париж на несколько дней, заметит покосившиеся фасады десятков домов, обескураженные хозяева которых не производят никакого ремонта. Эти здания остались от старинного квартала, который постепенно разрушается…», – так описал этот квартал Бальзак в романе «Кузина Бетта». Прошла пара лет после выхода книги в свет – и по приказу императора Наполеона III «рухлядь» снесли, квартал полностью уничтожили, а на месте его расположился новый центральный двор Лувра – «двор Наполеона». Именно этот свободный от строений двор и был раскопан археологами в последней четверти ХХ века, и оказалось, что круг замкнулся. Самая «молодая» часть ансамбля Лувра оказалась как раз над самой древней – над фундаментом того самого «заурядного средневекового замка».

<br>
<br>
Нужно признаться, что неуемные «строители Лувра» не остановились. После раскопок место «двора Наполеона» заняло весьма экстравагантное современное административное здание музея, и едва ли это последняя пристройка к дворцу… В годы кризисов и процветания, террора и реставрации то та, то иная часть Лувра обязательно стояла в лесах. Видимо, это естественное состояние Лувра, которое длится с 1190 года. Каждая эпоха добавляет свой кирпич.

<br>
<br>
<a STYLE="color:#000000; font-size:20px; font-weight:bold">История строительства Лувра</a>
<br>
<br>
Коллекции музея занимают большой комплекс зданий, строительство которых осуществлялось в течение 8 веков. Начало было положено королем Филиппом II Августом (годы правления 1180–1223), по приказу которого была построена крепость, защищавшая подступы к острову Ситэ, где тогда находится центр Парижа. За цитаделью закрепилось название Лувр, возможно потому, что в этой местности издавна водилось много волков, и ее называли «волчье место» – Лувения. Есть и другие версии происхождения наименования замка. 
<br>
<br>

Король Карл V (годы правления 1364–1380) сделал Лувр своей резиденцией, в связи с чем архитектору Р. дю Тамплю было поручено переделать и расширить замок.
<br>
<br>

При Франциске I, с 1527, начала осуществляться радикальная перестройка здания внутри и снаружи. Средневековые башни были снесены, и замок приобрел вид ренессансного дворца. Главные работы были осуществлены по проекту и под руководством П.Ляско.
<br>
<br>

В 1563 вдова Генриха II, Екатерина Медичи, поручила Филиппу Делорму возведение нового дворца. Он стал называться Тюильри, так как находился на месте бывшего черепичного завода (tuilerie). В 1871 дворец Тюильри сгорел и больше не восстанавливался. Екатерина МедичиПри Генрихе IV (годы правления 1589–1610) был составлен генеральный план, в результате осуществления которого общая площадь Лувра увеличилась в 4 раза. Между Лувром и Тюильри в 1608 вдоль берега Сены была возведена галерея (длина 420 м), получившая название Большой галереи. Она стала основой будущего музея, так как предполагалось, что здесь будут размещаться королевские коллекции.

<img src="http://aminpro.ru/dopoln/kartinki_d2/dopol/ludi/france/catherine_de_medicis.jpg" alt="" title="" align="right" border="1" vspace="15" hspace="15">

Во второй половине 17 в. в Лувре были предприняты широкомасштабные работы с целью приблизить облик дворца к архитектуре эпохи барокко. В Париж из Рима был для этого приглашен один из главных создателей этого стиля Л Бернини. Однако проект, предложенный им, сочли слишком помпезным. Работа была поручена французским архитекторам. К Перро (1613–1688) построил знаменитую восточную колоннаду в стиле классицизм, которому отдавали предпочтение во Франции. П Лево(1612–1670) создал ряд интерьеров, в т.ч. зал Августа, предназначенный для размещения королевских коллекций античных статуй, оружия, медалей. После пожара в 1661 Лево заново воссоздал галерею Аполлона, отделку и роспись которой осуществил Ш Лебрен.По его рисункам были исполнены живописные панно для плафонов, облицовка стен, рельефы, даже замки и ручки – все, вплоть до малейших деталей.
<br>

История приобретения коллекций Лувра</a>
<br>
<br>

Уже в 15 в. во Франции пробудился интерес к коллекционированию памятников искусства. В 15–16 вв., во время военных походов в Италию, французские короли не упускали возможности вывезти из этой страны античные статуи и другие художественные ценности. Так, король Карл VIII, захватив Флоренцию в 1494, перевез во Францию многие сокровища дворца Медичи и другие трофеи. Однако подлинным основоположником политики покровительства искусствам и планомерного приобретения коллекций был французский король Франциск I (годы правления 1515–1545). Он не только использовал любую возможность получить произведения искусства (главным образом, работы итальянских мастеров), но и стремился переселить во Францию самых лучших художников. Среди 74 иностранных художников, работавших в это время во Франции, 73 были итальянцами. В числе тех, кто прибыл в эту страну по приглашению короля, были
<a href="https://ru.wikipedia.org/wiki/%D0%9B%D0%B5%D0%BE%D0%BD%D0%B0%D1%80%D0%B4%D0%BE_%D0%B4%D0%B0_%D0%92%D0%B8%D0%BD%D1%87%D0%B8" STYLE="color:#000099">Леонардо да Винчи</a>
(годы пребывания во Франции 1517–1519), прославленный ювелир и скульптор из Флоренции Бенвенуто Челлини, Андреа дель Сарто,Россо, Приматиччо, Пермиджанино и многие другие.Леонардо да Винчи умер во Франции, все находившиеся в его мастерской картины были приобретены Франциском I, в том числе знаменитая Джоконда. Тогда же во Францию были привезены Рабы Микэланджело, позднее попавшие в коллекцию Лувра.

<a href="https://ru.wikipedia.org/wiki/Мона_Лиза" STYLE="color:#000099">Джоконда.</a>


Тогда же во Францию были привезены Рабы Микэланджело, позднее попавшие в коллекцию Лувра.
<br>
<br>

Пополнение коллекций продолжалось и в последующие годы. Мария Медичи – регентша при малолетнем короле Людовике XIII заказала П.Рубенсу цикл, состоящий из 21 картины с изображением событий ее жизни. Картины предназначались для Люксембургского дворца, в 1846 они были переданы в Лувр. Особенный размах собирательская деятельность получила в 17 в. Не только короли Франции, но и многие другие любители искусства имели богатейшие коллекции, например, всесильные министры: Ришелье, Мазарини, банкир Кроза. Большая часть коллекций Ришелье и Мозарини была унаследована Людовиком XIV. Король проявлял исключительный интерес к искусству, по его заказам французскими мастерами были созданы многочисленные картины, скульптуры, произведения декоративно-прикладного искусства, значительная часть которых сейчас находится в разных отделах Лувра. Тогда была приобретена большая часть коллекции картин Пуссена (у Людовика XIV их было 31), а также Рубенса. После смерти Рембрандта в 1669, Людовик XIV распорядился скупить все, что осталось в его мастерской. В течение 3 лет для королевской коллекции покупали картины этого мастера. 
<br>
<br>

Особенно усердно старался приумножить королевские коллекции первый министр короля Кольбер. За годы его деятельности они увеличились с 200 картин до 2000. Уже не столь масштабные приобретения делались и в последующие десятилетия. Тогда были куплены многие картины нидерландских мастеров 15–16 вв., ранее не привлекавшие к себе внимания.
 
<br>
<br>

После французской революции 1789, королевские коллекции, а также собрания, принадлежавшие эмигрантам, были национализированы, некоторые иконы и церковная утварь поступили из храмов. К 1793 в Лувре насчитывалось 500 картин и 155 других художественных предметов, большая часть из них происходили из королевских коллекций. Располагались они в основном в Большой галерее. 


<br>
<br>

Уже в период Директории было провозглашено, что Франция имеет суверенное право на произведения искусства из других стран, чтобы украшать «царство свободы». Первая партия захваченных французской армией художественных памятников поступила из Нидерландов в 1794. В 1796, после оккупации Италии французскими войсками под руководством Наполеона Бонапарта, из этой страны было вывезено все самое ценное из знаменитых коллекций Ватикана, виллы Боргезе, а также богатейших собраний Флоренции, Венеции и др. В Лувре тогда находились Аполлон Бельведерский, Лаокоон и др. Во Францию были отправлены многочисленные обозы с произведениями искусства из различных государств на территории Германии, из Австрии и др. стран Европы, в которые под звуки Марсельезы входила французская армия. 
<br>
<br>


В военной кампании Наполеона в Египет приняли участие археологи и ученые. Во Францию была отправлена большая партия памятников Древнего Египта, ставшая основой для образования нового отдела Лувра.
<br>
<br>

Лувр при Наполеоне Бонапарте был объявлен центральным художественным музеем Европы, он стал символом мирового господства и мощи империи. Директором Лувра стал Виван Денон, участвовавший в военных походах Наполеона, в т.ч., в Египет. Виван Денон очень энергично проводил политику расширения коллекций. При нем собрание Лувра было впервые систематизировано по географическому и хронологическому принципу. Для того, чтобы восполнить пробелы собрания музея, в 1811 он отправился в Италию и приобрел произведения Чимабуэ и Джотто, а также картины итальянских художников 15 в., ранее почти не представленных в Лувре. Это был один из первых примеров систематического пополнения коллекции в истории музейного дела.
<br>
<br>
После падения империи Наполеона Бонапарта многие вывезенные им из других стран коллекции были возвращены, однако далеко не все. Так, почти половина захваченных в Италии картин осталась в Лувре.

<br>
<br>
В последующие годы в Лувр стали поступать произведения мастеров Франции 19 в: Давида,Энгра,Делакруа. Многие частные коллекции были куплены дирекцией Лувра или поступили по завещанию, а также в качестве даров. Из этих источников была получена большая часть картин французских мастеров 18 в., а также такие шедевры как Цыганка Франса Хальса (написана в 1625, в Лувре с 1869), Король дураков И Босха (исполнен в 1500, в Лувре с 1918) и многие другие. 
<br>
<br>

Чрезвычайно обогатили музей французские археологи 19 в. Так, знаменитый ученый О.Мариэтт (1821–1881) производил раскопки в Мемфисе (Египте) и переправил в Лувр 6 тыс. найденных им памятников. Среди них – Сидящий писец. В 1819 французским послом была подарена королю Венера Милосская, найденная на греческом острове Милос крестьянами, а в 1863 была приобретена Ника Самофракийская, обнаруженная французскими археологами. В 1863 была куплена часть знаменитой коллекции маркиза Кампана (другая ее часть была приобретена Эрмитажем), откуда происходят многие знаменитые греческие вазы.
<br>
<br>
Раскопки, осуществленные в районе Двуречья французским консулом Ботта в середине 19 в., послужили толчком для начала широкомасштабных археологических работ в этом регионе. Очень важную роль в открытии и изучении цивилизаций древнего Востока сыграли французские ученые, такие как О.Мариетт, Г.Масперо. В связи с их археологическими находками был образован отдел Древнего Востока. В него поступили многие вещи, обнаруженные как французскими исследователями, так и археологами других стран.
<br>
<br>


Во время второй мировой войны, перед вступлением фашистской армии в Париж, коллекции Лувра были упакованы в 6 тысяч ящиков и спрятаны в тайниках средневековых замков  Они полностью сохранились, и после войны снова вернулись в залы музея. Некоторые коллекции были открыты для посещений уже в 1945.

<br>
<br>

С 1674 Людовик XIV принял решение сделать своей резиденцией Версаль. Работы в Лувре были приостановлены, многие помещения так и остались недостроенными в течение длительного времени
<br>
<br>

В связи с тем, что Лувр утратил значение королевской резиденции, его стали постепенно занимать различные учреждения. Здесь отводились помещения для мастерских художников и для квартирантов. В Лувре жили мебельщик Буль, знаменитый декоратор Берен, скульптор Жирадон, разместивший в Лувре собственную коллекцию, в которой даже была египетская мумия.
<br>
<br>


<a STYLE="color:#000000; font-size:20px; font-weight:bold">Лувр становится музеем</a>
<br>
<br>

Идея достойного служения идеалам Прогресса и, в связи с этим, превращения Лувра в общедоступный музей наук и искусств, была выдвинута французскими просветителями в середине 18 в. Художник Гюбер Робер предложил проект перестройки Большой галереи с целью создания в ней верхнего освещения через застекленный потолок (проект был осуществлен в начале 19 в.)

<br>
<br>

В 1793 часть залов Лувра была преобразована, и музей был открыт для публики. Однако работы в нем продолжались. Особенно грандиозный размах они приняли при Наполеоне Бонапарте. Крупнейшие архитекторы этой эпохи Ш Персье и П Фонтен значительно расширили площадь Лувра за счет новых пристроек. В это время была возведена еще одна галерея, параллельная Большой галерее. Работы по реконструкции Лувра и увеличению его площади продолжали осуществляться и в последующие десятилетия, особенно интенсивно они производились в годы правления Наполеона III (с 1848).
<br>
<br>

После падения его режима в 1870 началась реконструкция зданий Лувра и строительство новых помещений, которое продолжается вплоть до нашего времени. С тех пор как Лувр перестал служить одной из резиденций властей Франции, бывшие административные помещения стали постепенно освобождаться и передаваться музею. Этот процесс затянулся на долгие годы. Только в 1960–1980-е последние административные учреждения покинули Лувр. К 1980-м в распоряжение музея перешел весь комплекс зданий, включая северное крыло, где до недавнего времени располагалось министерство финансов.
<br>
<br>
Новый этап строительных работ наступил в 1980-е, когда началось осуществление проекта «Большого Лувра», предпринятого по инициативе президента Ф Миттерана: перепланировка центра Лувра и устройство нового входа в музей. Проект разработал американский архитектор китайского происхождения Ео Минг Пей. Он создал большую стеклянную пирамиду, поднявшуюся в центре Двора Наполеона, а вокруг нее три пирамиды поменьше. Рецепт стекла для пирамид был специально разработан в соответствии с новейшими технологиями так, что эта конструкция излучает свет. Между пирамидами расположился треугольный бассейн из темного камня, едва возвышающийся над уровнем земли. Завершается композиция, которую архитектор назвал «ландшафтом», фонтаном.
<br>
<br>

В настоящее время вход в музей расположен в большой пирамиде. В центральную колонну вмонтирован лифт. В главном вестибюле расположились различные службы музея, билетные кассы, доска объявлений, гардероб и др. По установленным здесь эскалаторам можно подняться в разные залы музея. Во время проведения строительных работ под двором Лувра, называемом «кур карре» – квадратный двор, были раскрыты фундаменты, часть стен и помещений старинной цитадели, построенной в 13 в. при Филиппе Августе. Этот комплекс, получивший название «Средневековый Лувр», стал новым подразделением музея.

